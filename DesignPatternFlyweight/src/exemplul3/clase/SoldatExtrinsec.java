package exemplul3.clase;

public class SoldatExtrinsec {
    private String nume;
    private int x;
    private int y;

    public SoldatExtrinsec(String nume, int x, int y) {
        this.nume = nume;
        this.x = x;
        this.y = y;
    }

    public String getNume() {
        return nume;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
